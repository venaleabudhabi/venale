export const useTranslation = (lang: 'en' | 'ar') => {
  return {
    t: (key: string): string => {
      const keys = key.split('.');
      let value: any = translations[lang];
      for (const k of keys) {
        value = value?.[k];
      }
      return value || key;
    },
    dir: lang === 'ar' ? 'rtl' : 'ltr',
  };
};
